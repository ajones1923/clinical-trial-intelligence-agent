# HELPER FUNCTIONS
# ===================================================================


def _now_iso() -> str:
    """Return the current UTC timestamp in ISO-8601 format."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _now_display() -> str:
    """Return a human-friendly UTC timestamp."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")


def _severity_badge(severity: SeverityLevel) -> str:
    """Return a Markdown badge string for a severity level."""
    colour = SEVERITY_COLORS.get(severity, "#95A5A6")
    label = severity.value.replace("_", " ").title()
    return f"![{label}](https://img.shields.io/badge/Severity-{label.replace(' ', '%20')}-{colour[1:]})"


def _severity_indicator(severity: SeverityLevel) -> str:
    """Return a text indicator for severity."""
    mapping = {
        SeverityLevel.LOW: "[LOW]",
        SeverityLevel.INFORMATIONAL: "[INFO]",
        SeverityLevel.MODERATE: "[MODERATE]",
        SeverityLevel.HIGH: "[HIGH]",
        SeverityLevel.CRITICAL: "[CRITICAL]",
    }
    return mapping.get(severity, "[UNKNOWN]")


def _generate_filename(prefix: str, extension: str) -> str:
    """Generate a timestamped filename for export."""
    ts = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
    return f"{prefix}_{ts}.{extension}"


